<!-- JavaScript for filtering -->
<script>
    document.addEventListener('DOMContentLoaded', function() {
        const searchInput = document.getElementById('search');
        const statusFilter = document.getElementById('status-filter');
        const categoryFilter = document.getElementById('category-filter');

        function applyFilters() {
            const searchTerm = searchInput.value.toLowerCase();
            const statusValue = statusFilter.value;
            const categoryValue = categoryFilter.value;

            const rows = document.querySelectorAll('tbody tr');

            rows.forEach(row => {
                const eventName = row.querySelector('.text-sm.font-medium').textContent.toLowerCase();
                const status = row.querySelector('.px-2.inline-flex').textContent.toLowerCase();
                const category = row.querySelector('td:nth-child(2) .text-sm').textContent;

                const matchesSearch = eventName.includes(searchTerm);
                const matchesStatus = !statusValue || status === statusValue;
                const matchesCategory = !categoryValue || category === categoryValue;

                if (matchesSearch && matchesStatus && matchesCategory) {
                    row.style.display = '';
                } else {
                    row.style.display = 'none';
                }
            });
        }

        searchInput.addEventListener('input', applyFilters);
        statusFilter.addEventListener('change', applyFilters);
        categoryFilter.addEventListener('change', applyFilters);
    });
</script>
